// 安全輸出 HTML：轉義特殊字元，但保留 <br> 換行標籤
function safeHtml(str) {
    if (!str) return '';

    return str
        .replace(/&/g, '&amp;')                   // & → &amp;
        .replace(/</g, '&lt;')                    // < → &lt;
        .replace(/>/g, '&gt;')                    // > → &gt;
        .replace(/&lt;br\s*\/?&gt;/gi, '<br>');   // 恢復 <br> 標籤
}